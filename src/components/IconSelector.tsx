export function IconSelector({
  selectedIcon,
  onChange
}: {
  selectedIcon?: string;
  onChange: (icon: string) => void;
}) {
  return (
    <div
      className="grid grid-cols-6 gap-4 overflow-auto"
      style={{ maxHeight: '200px' }}
    >
      {Object.entries(iconList).map(([iconName, IconComponent]) => (
        <button
          key={iconName}
          type="button"
          onClick={() => onChange(iconName)}
          className={cn(
            'rounded-md border p-2',
            selectedIcon === iconName
              ? 'border-blue-500 bg-blue-100'
              : 'border-gray-300'
          )}
        >
          <IconComponent className="h-6 w-6" />
        </button>
      ))}
    </div>
  );
}
